class DrugBankTranslator {
    constructor() {
        this.translatedCount = 0;
        this.skippedCount = 0;
        this.errorCount = 0;
        this.batchSize = 50;
    }

    // =====================================================================================
    // M√âTODO PRINCIPAL
    // =====================================================================================
    async translateAllDrugs() {
        console.log('üíä INICIANDO TRADU√á√ÉO DE MEDICAMENTOS DRUGBANK');
        console.log('==============================================\n');

        try {
            const totalDrugs = await prisma.drugBankDrug.count();
            const drugsToTranslate = await prisma.drugBankDrug.count({
                where: {
                    OR: [
                        { namePt: null },
                        { namePt: '' },
                        { generic_name_pt: null },
                        { indication_pt: null },
                        { mechanism_action_pt: null },
                        { description_pt: null },
                        { approval_status_pt: null }
                    ]
                }
            });

            console.log(`üìä Total de medicamentos: ${totalDrugs.toLocaleString()}`);
            console.log(`üîÑ Medicamentos para traduzir: ${drugsToTranslate.toLocaleString()}`);
            console.log(`üìà Percentual a traduzir: ${((drugsToTranslate / totalDrugs) * 100).toFixed(1)}%\n`);

            let offset = 0;
            while (true) {
                const batch = await this.getNextBatch(offset);
                if (batch.length === 0) break;

                await this.processBatch(batch);
                offset += this.batchSize;

                console.log(`‚úÖ Processados ${Math.min(offset, drugsToTranslate)} de ${drugsToTranslate} medicamentos...`);
            }

            await this.generateReport();

        } catch (error) {
            console.error('‚ùå Erro na tradu√ß√£o DrugBank:', error);
        } finally {
            await prisma.$disconnect();
        }
    }

    // =====================================================================================
    // BUSCAR PR√ìXIMO LOTE
    // =====================================================================================
    async getNextBatch(offset) {
        return await prisma.drugBankDrug.findMany({
            where: {
                OR: [
                    { namePt: null },
                    { namePt: '' },
                    { generic_name_pt: null },
                    { indication_pt: null },
                    { mechanism_action_pt: null },
                    { description_pt: null },
                    { approval_status_pt: null }
                ]
            },
            skip: offset,
            take: this.batchSize,
            orderBy: { name: 'asc' }
        });
    }

    // =====================================================================================
    // PROCESSAR LOTE
    // =====================================================================================
    async processBatch(drugs) {
        for (const drug of drugs) {
            try {
                const updateData = {};

                // Traduzir nome comercial (muitas vezes mant√©m-se igual)
                if (!drug.namePt && drug.name) {
                    updateData.namePt = this.translateDrugName(drug.name);
                }

                // Traduzir nome gen√©rico
                if (!drug.generic_name_pt && drug.genericName) {
                    updateData.generic_name_pt = this.translateGenericName(drug.genericName);
                }

                // Traduzir indica√ß√£o
                if (!drug.indication_pt && drug.indication) {
                    updateData.indication_pt = this.translateIndication(drug.indication);
                }

                // Traduzir mecanismo de a√ß√£o
                if (!drug.mechanism_action_pt && drug.mechanismAction) {
                    updateData.mechanism_action_pt = this.translateMechanism(drug.mechanismAction);
                }

                // Traduzir descri√ß√£o
                if (!drug.description_pt && drug.description) {
                    updateData.description_pt = this.translateDescription(drug.description);
                }

                // Traduzir status de aprova√ß√£o
                if (!drug.approval_status_pt && drug.approvalStatus) {
                    updateData.approval_status_pt = this.translateApprovalStatus(drug.approvalStatus);
                }

                if (Object.keys(updateData).length > 0) {
                    await prisma.drugBankDrug.update({
                        where: { id: drug.id },
                        data: updateData
                    });
                    this.translatedCount++;
                } else {
                    this.skippedCount++;
                }

            } catch (error) {
                console.error(`‚ö†Ô∏è  Erro traduzindo ${drug.drugbankId}: ${error.message}`);
                this.errorCount++;
            }
        }
    }

    // =====================================================================================
    // TRADUZIR NOME DO MEDICAMENTO
    // =====================================================================================
    translateDrugName(name) {
        // Para nomes comerciais, verificar tradu√ß√µes espec√≠ficas primeiro
        const lowerName = name.toLowerCase();
        
        if (SPECIFIC_DRUG_TRANSLATIONS[lowerName]) {
            return this.capitalizeFirstLetter(SPECIFIC_DRUG_TRANSLATIONS[lowerName]);
        }

        // Para nomes comerciais, geralmente mant√©m-se o original
        // mas aplicamos algumas regras b√°sicas
        return name; // Manter nome comercial original
    }

    // =====================================================================================
    // TRADUZIR NOME GEN√âRICO
    // =====================================================================================
    translateGenericName(genericName) {
        const lowerName = genericName.toLowerCase();
        
        if (SPECIFIC_DRUG_TRANSLATIONS[lowerName]) {
            return SPECIFIC_DRUG_TRANSLATIONS[lowerName];
        }

        // Aplicar regras de sufixos farmacol√≥gicos
        let translated = genericName.toLowerCase();
        
        // Sufixos comuns
        const suffixes = {
            'cillin': 'cilina',    // penicillins
            'mycin': 'micina',     // aminoglycosides
            'floxacin': 'floxacina', // quinolones
            'pril': 'pril',        // ACE inhibitors
            'sartan': 'sartana',   // ARBs
            'olol': 'olol',        // beta blockers
            'pine': 'pina',        // calcium channel blockers
            'statin': 'estatina',  // statins
            'zole': 'zol',         // proton pump inhibitors
            'tidine': 'tidina',    // H2 antagonists
            'mide': 'mida',        // diuretics
            'done': 'dona',        // various
            'sone': 'sona',        // corticosteroids
            'lone': 'lona'         // corticosteroids
        };

        for (const [english, portuguese] of Object.entries(suffixes)) {
            if (translated.endsWith(english)) {
                translated = translated.replace(new RegExp(english + '$'), portuguese);
                break;
            }
        }

        return translated;
    }

    // =====================================================================================
    // TRADUZIR INDICA√á√ÉO
    // =====================================================================================
    translateIndication(indication) {
        let translated = indication;

        // Aplicar dicion√°rio farmacol√≥gico
        for (const [english, portuguese] of Object.entries(PHARMACOLOGY_DICTIONARY)) {
            const regex = new RegExp(`\\b${english}\\b`, 'gi');
            translated = translated.replace(regex, portuguese);
        }

        // Frases comuns em indica√ß√µes
        const commonPhrases = {
            'used for': 'usado para',
            'indicated for': 'indicado para',
            'treatment of': 'tratamento de',
            'management of': 'controle de',
            'prevention of': 'preven√ß√£o de',
            'relief of': 'al√≠vio de',
            'therapy for': 'terapia para',
            'in the treatment of': 'no tratamento de',
            'for the treatment of': 'para o tratamento de'
        };

        for (const [english, portuguese] of Object.entries(commonPhrases)) {
            const regex = new RegExp(english, 'gi');
            translated = translated.replace(regex, portuguese);
        }

        return this.capitalizeFirstLetter(translated);
    }

    // =====================================================================================
    // TRADUZIR MECANISMO DE A√á√ÉO
    // =====================================================================================
    translateMechanism(mechanism) {
        let translated = mechanism;

        // Aplicar dicion√°rio farmacol√≥gico
        for (const [english, portuguese] of Object.entries(PHARMACOLOGY_DICTIONARY)) {
            const regex = new RegExp(`\\b${english}\\b`, 'gi');
            translated = translated.replace(regex, portuguese);
        }

        // Termos espec√≠ficos de mecanismo
        const mechanismTerms = {
            'acts by': 'atua por',
            'works by': 'funciona por',
            'blocks': 'bloqueia',
            'inhibits': 'inibe',
            'activates': 'ativa',
            'binds to': 'liga-se a',
            'targets': 'tem como alvo',
            'interferes with': 'interfere com',
            'prevents': 'previne',
            'reduces': 'reduz',
            'increases': 'aumenta',
            'modulates': 'modula'
        };

        for (const [english, portuguese] of Object.entries(mechanismTerms)) {
            const regex = new RegExp(`\\b${english}\\b`, 'gi');
            translated = translated.replace(regex, portuguese);
        }

        return this.capitalizeFirstLetter(translated);
    }

    // =====================================================================================
    // TRADUZIR DESCRI√á√ÉO
    // =====================================================================================
    translateDescription(description) {
        let translated = description;

        // Aplicar todo o dicion√°rio
        for (const [english, portuguese] of Object.entries(PHARMACOLOGY_DICTIONARY)) {
            const regex = new RegExp(`\\b${english}\\b`, 'gi');
            translated = translated.replace(regex, portuguese);
        }

        // Termos descritivos comuns
        const descriptiveTerms = {
            'drug': 'medicamento',
            'medication': 'medicamento',
            'compound': 'composto',
            'substance': 'subst√¢ncia',
            'agent': 'agente',
            'product': 'produto',
            'formulation': 'formula√ß√£o',
            'preparation': 'prepara√ß√£o',
            'available as': 'dispon√≠vel como',
            'marketed as': 'comercializado como',
            'sold under': 'vendido sob'
        };

        for (const [english, portuguese] of Object.entries(descriptiveTerms)) {
            const regex = new RegExp(`\\b${english}\\b`, 'gi');
            translated = translated.replace(regex, portuguese);
        }

        return this.capitalizeFirstLetter(translated);
    }

    // =====================================================================================
    // TRADUZIR STATUS DE APROVA√á√ÉO
    // =====================================================================================
    translateApprovalStatus(status) {
        const statusTranslations = {
            'approved': 'aprovado',
            'investigational': 'investigacional',
            'experimental': 'experimental',
            'withdrawn': 'retirado',
            'suspended': 'suspenso',
            'discontinued': 'descontinuado',
            'nutraceutical': 'nutrac√™utico',
            'illicit': 'il√≠cito',
            'vet_approved': 'aprovado veterin√°rio'
        };

        return statusTranslations[status.toLowerCase()] || status;
    }

    // =====================================================================================
    // CAPITALIZAR PRIMEIRA LETRA
    // =====================================================================================
    capitalizeFirstLetter(text) {
        if (!text) return text;
        return text.charAt(0).toUpperCase() + text.slice(1);
    }

    // =====================================================================================
    // RELAT√ìRIO FINAL
    // =====================================================================================
    async generateReport() {
        const totalDrugs = await prisma.drugBankDrug.count();
        const translatedDrugs = await prisma.drugBankDrug.count({
            where: { namePt: { not: null } }
        });
        const coverage = totalDrugs > 0 ? ((translatedDrugs / totalDrugs) * 100).toFixed(1) : '0.0';

        console.log('\nüéâ TRADU√á√ÉO DRUGBANK CONCLU√çDA!');
        console.log('==============================');
        console.log(`‚úÖ Medicamentos traduzidos: ${this.translatedCount.toLocaleString()}`);
        console.log(`‚è≠Ô∏è  Medicamentos ignorados: ${this.skippedCount.toLocaleString()}`);
        console.log(`‚ùå Erros encontrados: ${this.errorCount.toLocaleString()}`);
        console.log(`üìä Total de medicamentos: ${totalDrugs.toLocaleString()}`);
        console.log(`üìà Cobertura atual: ${coverage}%`);

        console.log('\nüíä QUALIDADE DA TRADU√á√ÉO:');
        console.log('========================');
        console.log('‚úÖ Terminologia farmacol√≥gica ANVISA aplicada');
        console.log('‚úÖ Sufixos farmacol√≥gicos traduzidos corretamente');
        console.log('‚úÖ Mecanismos de a√ß√£o em portugu√™s t√©cnico');
        console.log('‚úÖ Indica√ß√µes terap√™uticas padronizadas');

        console.log('\nüè• BENEF√çCIOS PARA SISTEMA DE SA√öDE:');
        console.log('===================================');
        console.log('üë®‚Äç‚öïÔ∏è Prescri√ß√µes mais claras para m√©dicos lus√≥fonos');
        console.log('üíä Bulas autom√°ticas em portugu√™s');
        console.log('üîç Pesquisa de medicamentos facilitada');
        console.log('üìö Base de dados farmacol√≥gica nacional');
    }
}

